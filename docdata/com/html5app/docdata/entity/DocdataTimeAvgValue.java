package com.html5app.docdata.entity;
/**
 * 某个日期的小时平均值
 * @author SQ
 * 2017年3月3日
 * 下午4:02:43
 */
public class DocdataTimeAvgValue {
		private String valueavg;
		private String hh;
		private String DocTagName;
		
		
		public String getValueavg() {
			return valueavg;
		}
		public void setValueavg(String valueavg) {
			this.valueavg = valueavg;
		}
		public String getHh() {
			return hh;
		}
		public void setHh(String hh) {
			this.hh = hh;
		}
		public String getDocTagName() {
			return DocTagName;
		}
		public void setDocTagName(String docTagName) {
			DocTagName = docTagName;
		}
		
		
}
